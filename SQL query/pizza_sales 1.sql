-- create database
CREATE database PIZZA;
USE PIZZA;

-- Create the ORDERS table
CREATE TABLE ORDERS (
    ORDER_ID INT PRIMARY KEY,
    ORDER_DATE DATE,
    ORDER_TIME TIME
);


-- Create the PIZZA_TYPES table
CREATE TABLE PIZZA_TYPES (
    PIZZA_TYPE_ID VARCHAR(25) PRIMARY KEY,
    NAME VARCHAR(25),
    CATEGORY VARCHAR(25),
    INGREDIENTS VARCHAR(55)
);
ALTER TABLE PIZZA_TYPES
MODIFY COLUMN ingredients VARCHAR(200);

ALTER TABLE PIZZA_TYPES
MODIFY COLUMN name VARCHAR(200);
ALTER TABLE PIZZA_TYPES
MODIFY COLUMN category VARCHAR(200);
ALTER TABLE PIZZA_TYPES
MODIFY COLUMN pizza_type_id VARCHAR(200) primary key;

-- Create the PIZZAS table
CREATE TABLE PIZZAS (
    PIZZA_ID VARCHAR(25) PRIMARY KEY,
    SIZE VARCHAR(25),
    PRICE INT,
    PIZZA_TYPE_ID VARCHAR(25),
    FOREIGN KEY (PIZZA_TYPE_ID) REFERENCES PIZZA_TYPES(PIZZA_TYPE_ID)
);

-- Create the ORDER_details table
CREATE TABLE ORDER_DETAILS (
    ORDER_DETAIL_ID INT PRIMARY KEY AUTO_INCREMENT,
    ORDER_ID INT,
    PIZZA_ID VARCHAR(25),
    QUANTITY INT,
    FOREIGN KEY (ORDER_ID) REFERENCES ORDERS(ORDER_ID),
    FOREIGN KEY (PIZZA_ID) REFERENCES PIZZAS(PIZZA_ID)
);


